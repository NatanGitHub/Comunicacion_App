package com.bsav157.comunicacion_app;

public class Productos {

    private String nombre;
    private long precio;

    public Productos() {

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public long getPrecio() {
        return precio;
    }

    public void setPrecio(long precio) {
        this.precio = precio;
    }

}
